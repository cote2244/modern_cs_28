# รับค่าราคาสินค้า (ใช้ float เพื่อรองรับทศนิยม)
price = float(input())

# ตรวจสอบเงื่อนไข: ถ้าราคามากกว่าหรือเท่ากับ 1000
if price >= 1000:
    # ลดราคา 10% (คือจ่ายจริง 90% ของราคาเต็ม)
    final_price = price * 0.90
else:
    # ไม่ลดราคา
    final_price = price

# แสดงผลทศนิยม 2 ตำแหน่ง
print(f"{final_price:.2f}")